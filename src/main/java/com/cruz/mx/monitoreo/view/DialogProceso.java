    private boolean validateEmptyField(JTextField field){
        boolean result = !"".equals(field.getText());
        if(!result){
            JOptionPane.showMessageDialog(this, "Favor de llenar todos los campos.", "Validación", JOptionPane.WARNING_MESSAGE);
            field.requestFocus();
        }
        return result;
    }
    
    public void setMode(DIALOG_STATE mode){
        this.mode = mode;
        setVisible(true);
        setLocationRelativeTo(principal);
        btnAgregar.setText((mode.equals(DIALOG_STATE.NEW)? "Agregar": "Actualizar"));
        setTitle((mode.equals(DIALOG_STATE.NEW)? "Nueva preferencia": "Actualizar preferencia"));
        if(mode.equals(DIALOG_STATE.NEW)){
            btnEliminar.setVisible(false);
        }
        else{
            btnEliminar.setVisible(true);
        }
    }
    
    public void setData(Proceso proceso){
        this.proceso = proceso;
        comboSistema.setSelectedItem(proceso.getSistema());
        comboFrecuencia.setSelectedItem(proceso.getFrecuencia());
        textFieldAlerta.setText(String.valueOf(proceso.getNumAlerta()));
        textFieldTexto.setText(proceso.getTextoBusqueda());
        textFieldMensaje.setText(proceso.getMensaje());
        String rango[] = proceso.getRangoTiempo().split(RANGE_SEPARATOR);
        rangerSlider.setTimeRange(Integer.valueOf(rango[0].split(":")[0]), Integer.valueOf(rango[1].split(":")[0]));
    }
    
    private void emptyData(){
        textFieldAlerta.setText("");
        textFieldMensaje.setText("");
        textFieldTexto.setText("");
        comboSistema.setSelectedIndex(0);
        comboFrecuencia.setSelectedIndex(0);
        rangerSlider.setTimeRange(9, 19);
    }
    
