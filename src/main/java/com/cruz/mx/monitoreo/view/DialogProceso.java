    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(validateEmptyField(textFieldAlerta) && validateEmptyField(textFieldMensaje) && validateEmptyField(textFieldTexto)){
            String numAlerta = textFieldAlerta.getText();
            String mensaje = textFieldMensaje.getText();
            String texto = textFieldTexto.getText();
            int frecuencia = Integer.parseInt(comboFrecuencia.getSelectedItem().toString());
            String sistema = comboSistema.getSelectedItem().toString();
            String rango = rangerSlider.getDesde() + RANGE_SEPARATOR + rangerSlider.getHasta();
            if(!numAlerta.matches("\\d+")){
                JOptionPane.showMessageDialog(this, "Favor de ingresar un número entero.", "Campo incorrecto", JOptionPane.ERROR_MESSAGE);
                textFieldAlerta.requestFocus();
                return;
            }
            if(mode.equals(DIALOG_STATE.NEW)){
                Proceso proceso = new Proceso(sistema, frecuencia, rango, Integer.parseInt(numAlerta), texto, mensaje);
                principal.addProcesoTabla(proceso);
            }
            else{
                proceso.setFrecuencia(frecuencia);
                proceso.setMensaje(mensaje);
                proceso.setNumAlerta(Integer.parseInt(numAlerta));
                proceso.setRangoTiempo(rango);
                proceso.setSistema(sistema);
                proceso.setTextoBusqueda(texto);
                proceso.setRunning(false);//
                principal.updateProcesoTabla(proceso);
            }
            this.dispose();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(JOptionPane.YES_OPTION == JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar la preferencia?", "Eliminar preferencia", JOptionPane.YES_NO_OPTION)){
            proceso.setRunning(false);
            principal.deleteProcesoTabla(proceso);
            this.dispose();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

